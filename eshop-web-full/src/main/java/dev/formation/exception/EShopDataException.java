package dev.formation.exception;

public class EShopDataException extends EShopException {
	private static final long serialVersionUID = 1L;

	public EShopDataException() {
		super();
	}

	public EShopDataException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public EShopDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public EShopDataException(String message) {
		super(message);
	}

	public EShopDataException(Throwable cause) {
		super(cause);
	}

	
}
